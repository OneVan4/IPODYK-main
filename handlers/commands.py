    product_id = await db.add_product(url, message.from_user.id)
    if product_id:
        await message.reply(f"‚úÖ –¢–æ–≤–∞—Ä –¥–æ–±–∞–≤–ª–µ–Ω –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è. ID: {hbold(product_id)}")
